package week1.day1;

public class PostiveOrNegativeNum {
	
	public static void main(String[] args) {
		
		float getNumber = -34.78786f;
		if(getNumber > 0) {
			System.out.println("Positive Number");
		}	
		else if(getNumber < 0) {
			System.out.println("Negative Number");
		}
		else {
			System.out.println("zero");
		}
}

}
